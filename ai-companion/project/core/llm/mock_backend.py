class MockBackend(LLMBackend):
    """规则模板兜底后端。明确用于测试/演示，不伪装成真实模型。"""

    name = "mock"

    def __init__(self, settings=None, seed: int | None = None):
        # 保留 settings 引用：关系身份/自称在每次生成时实时读取，
        # 支持 UI 切换 active_relationship 后热更新（无需重建后端）。
        self._settings = settings
        self.master_name = getattr(settings, "master_name", None) or "主人"
        self._rng = random.Random(seed)

    # ------------------------------------------------------------ 上下文
    def _relationship(self) -> str:
        rel = getattr(self._settings, "active_relationship", None) or _DEFAULT_RELATIONSHIP
        return rel if rel in _RULES else _DEFAULT_RELATIONSHIP

    def _master(self) -> str:
        return getattr(self._settings, "master_name", None) or self.master_name

    def _self_ref(self) -> str:
        """自称的轻量性别替换：女性人格→「人家」，男性人格→「我」。"""
        pid = str(getattr(self._settings, "active_persona", "") or "")
        return "我" if pid.startswith("male") else "人家"

    def _render(self, text: str) -> str:
        master = self._master()
        # 模板以「主人」书写：自定义称呼时整体替换（与 persona 称呼替换一致）；
        # 同时兼容 {master} 占位写法。
        if master != "主人":
            text = text.replace("主人", master)
        return (text.replace("{master}", master)
                    .replace("{自称}", self._self_ref()))

    # ------------------------------------------------------------ 选模板
    def _pick(self, messages: list[dict]) -> tuple[str, str]:
        user_text = ""
        for msg in reversed(messages):
            if msg.get("role") == "user":
                user_text = str(msg.get("content", ""))
                break
        haystack = user_text.lower()
        scenes = _RULES[self._relationship()]
        entry = None
        for scene, keywords in _SCENE_KEYWORDS.items():
            if any(kw in haystack for kw in keywords):
                entry = scenes[scene]
                break
        if entry is None:
            entry = scenes["fallback"]
        thinking = entry["thinking"]
        reply = self._rng.choice(entry["replies"])
        return self._render(thinking), self._render(reply)

    # ------------------------------------------------------------ 契约
    def generate(self, messages: list[dict], temperature: float = 0.7,
                 max_tokens: int = 1024) -> GenerateResult:
        thinking, reply = self._pick(messages)
        raw = f"<think>{thinking}</think>{reply}"
        return GenerateResult(
            text=raw, reasoning="", model=f"mock:{self.name}",
            tokens=max(1, len(raw) // 2),
        )

    def generate_stream(self, messages: list[dict], **kw) -> Iterator[dict]:
        thinking, reply = self._pick(messages)
        for i in range(0, len(thinking), 4):
            yield {"type": "thinking", "delta": thinking[i:i + 4]}
        for i in range(0, len(reply), 4):
            yield {"type": "text", "delta": reply[i:i + 4]}

    def health_check(self) -> bool:
        return True
